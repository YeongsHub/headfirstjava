public class BooleanText {
    public static void main(String[] args){
        int x = 4 ;
        while (x > 3) {
            x--;
            System.out.println("Current value of x: " + x);
        }
        int z = 17;
        while (z == 17) {
            System.out.println("z is 17");
            z++;
        }
    }
}
